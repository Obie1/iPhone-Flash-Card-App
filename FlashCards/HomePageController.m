#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    // if selected subjects go to subject list view controller
    if (indexPath.row == 0)
    {
        // take segue to subject list controller
        [self performSegueWithIdentifier:@"pushToSubjectList" sender:self];
    }
    else if (indexPath.row == 1)
    {
        // take segue to about page
        [self performSegueWithIdentifier:@"pushToAboutPage" sender:self];
    }
}

